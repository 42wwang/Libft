#include "libft.h"

char	*ft_strchr(const char *s, int a)
{
	int	i;

	i = 0;
	while (s[i] != a)
	{
		if (s[i] == '\0')
			return (NULL);
		i++;
	}
	return ((char *)(s + i));
}
