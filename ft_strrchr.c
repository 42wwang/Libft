#include "libft.h"

char	*ft_strrchr(const char *s, int a)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == (char) a)
			return ((char *)(s + i));
		i--;
	}
	return (NULL);
}
